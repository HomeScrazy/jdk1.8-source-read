        // Red-black tree methods, all adapted from CLR

        static <K,V> TreeNode<K,V> rotateLeft(TreeNode<K,V> root,
                                              TreeNode<K,V> p) {

            //这里的 p 就是旋转点
            TreeNode<K,V> r, pp, rl;
            if (p != null && (r = p.right) != null) {
                //
                if ((rl = p.right = r.left) != null)
                    rl.parent = p;
                if ((pp = r.parent = p.parent) == null)
                    (root = r).red = false;
                else if (pp.left == p)
                    pp.left = r;
                else
                    pp.right = r;
                r.left = p;
                p.parent = r;
            }
            return root;
        }

        static <K,V> TreeNode<K,V> rotateRight(TreeNode<K,V> root,
                                               TreeNode<K,V> p) {
            TreeNode<K,V> l, pp, lr;
            if (p != null && (l = p.left) != null) {
                if ((lr = p.left = l.right) != null)
                    lr.parent = p;
                if ((pp = l.parent = p.parent) == null)
                    (root = l).red = false;
                else if (pp.right == p)
                    pp.right = l;
                else
                    pp.left = l;
                l.right = p;
                p.parent = l;
            }
            return root;
        }

        /*
         * 红黑树的再平衡操作。
         * 从指定的节点开始，判断是否平衡，如果不平衡，则需要进行再平衡操作
         * 再平衡操作有可能导致 其 父节点的不平衡，所以要继续向上进行再平衡操作。
         * 这里的变量名称很有特色
         * x 表示当前节点
         * xp 表示当前节点的父节点。xpp 则是 xp 的父节点以此类推
         * xr 表示 x 的 右节点 xpr 表示 xp 的右节点，其他一次类推
         * 这里对所有的不平衡情况都做了处理
         */
        static <K,V> TreeNode<K,V> balanceInsertion(TreeNode<K,V> root,
                                                    TreeNode<K,V> x) {
            x.red = true;
            for (TreeNode<K,V> xp, xpp, xppl, xppr;;) {
                if ((xp = x.parent) == null) {
                    //这种情况，说明目前 x 时 root 节点，直接将其染成黑色
                    x.red = false;
                    return x;
                }
                else if (!xp.red || (xpp = xp.parent) == null)
                    // xp 是 root 的情况
                    return root;
                if (xp == (xppl = xpp.left)) {
                    //需要注意的是，xpp 肯定是黑色的
                    // xp 是 xpp 的左子节点 且 xp 是　red 的
                    if ((xppr = xpp.right) != null && xppr.red) {
                        // xppr 也是 red
                        /*
                         * 这种情况，2-3-4 平衡树来讲，xpp 是 黑色的，xppl 和 xppr 都是红色的
                         * 此时在插入一个 red 节点 在 xpl 上，会导致 2-3-4 数出现一个节点 4 个数的情况
                         * 所以这里需要将 xpp 脱离出去，变成红色和它的父节点合并，和上面的节点合并，同时 xppl 和 xppr 都染成黑色
                         */
                        xppr.red = false;
                        xp.red = false;
                        xpp.red = true;
                        //xpp 脱离和父节点合并的情况没有在这里实现，而是 x 指向 xpp 后在做处理
                        //比如 如果 xpp 的 父节点也是红色的，后面就会做相应的处理
                        x = xpp;
                    }
                    else {
                        //xppr 是空或者是黑色节点的情况
                        //这种情况 xpp 是黑色 xp 是红色 xppr 是空或者黑色
                        if (x == xp.right) {
                            //如果 x 是 xp 的右节点
                            /*
                             *           xpp
                             *        xp
                             *           x
                             *
                             * 这种情况只要以 xp 为中心 变成
                             *
                             *      xp
                             *   x     xpp
                             */
                            root = rotateLeft(root, x = xp);
                            xpp = (xp = x.parent) == null ? null : xp.parent;
                        }
                        if (xp != null) {
                            //上面的 if 走完后也会走到这里，这时候会把 xp 染成黑色
                            //同时如果 x 是 左节点
                            /*
                             *           xpp
                             *         xp
                             *       x
                             */
                            xp.red = false;
                            if (xpp != null) {
                                // xpp != null 是上面进行了左旋的情况，这种个情况，需要将 xpp 变成 red
                                xpp.red = true;
                                root = rotateRight(root, xpp);
                            }
                        }
                    }
                }
                else {
                    //如果 xp 是 xpp 的右子节点的情况
                    if (xppl != null && xppl.red) {
                        xppl.red = false;
                        xp.red = false;
                        xpp.red = true;
                        x = xpp;
                    }
                    else {
                        if (x == xp.left) {
                            root = rotateRight(root, x = xp);
                            xpp = (xp = x.parent) == null ? null : xp.parent;
                        }
                        if (xp != null) {
                            xp.red = false;
                            if (xpp != null) {
                                xpp.red = true;
                                root = rotateLeft(root, xpp);
                            }
                        }
                    }
                }
            }
        }

        static <K,V> TreeNode<K,V> balanceDeletion(TreeNode<K,V> root,
                                                   TreeNode<K,V> x) {
            for (TreeNode<K,V> xp, xpl, xpr;;)  {
                if (x == null || x == root)
                    return root;
                else if ((xp = x.parent) == null) {
                    x.red = false;
                    return x;
                }
                else if (x.red) {
                    x.red = false;
                    return root;
                }
                else if ((xpl = xp.left) == x) {
                    if ((xpr = xp.right) != null && xpr.red) {
                        xpr.red = false;
                        xp.red = true;
                        root = rotateLeft(root, xp);
                        xpr = (xp = x.parent) == null ? null : xp.right;
                    }
                    if (xpr == null)
                        x = xp;
                    else {
                        TreeNode<K,V> sl = xpr.left, sr = xpr.right;
                        if ((sr == null || !sr.red) &&
                            (sl == null || !sl.red)) {
                            xpr.red = true;
                            x = xp;
                        }
                        else {
                            if (sr == null || !sr.red) {
                                if (sl != null)
                                    sl.red = false;
                                xpr.red = true;
                                root = rotateRight(root, xpr);
                                xpr = (xp = x.parent) == null ?
                                    null : xp.right;
                            }
                            if (xpr != null) {
                                xpr.red = (xp == null) ? false : xp.red;
                                if ((sr = xpr.right) != null)
                                    sr.red = false;
                            }
                            if (xp != null) {
                                xp.red = false;
                                root = rotateLeft(root, xp);
                            }
                            x = root;
                        }
                    }
                }
                else { // symmetric
                    if (xpl != null && xpl.red) {
                        xpl.red = false;
                        xp.red = true;
                        root = rotateRight(root, xp);
                        xpl = (xp = x.parent) == null ? null : xp.left;
                    }
                    if (xpl == null)
                        x = xp;
                    else {
                        TreeNode<K,V> sl = xpl.left, sr = xpl.right;
                        if ((sl == null || !sl.red) &&
                            (sr == null || !sr.red)) {
                            xpl.red = true;
                            x = xp;
                        }
                        else {
                            if (sl == null || !sl.red) {
                                if (sr != null)
                                    sr.red = false;
                                xpl.red = true;
                                root = rotateLeft(root, xpl);
                                xpl = (xp = x.parent) == null ?
                                    null : xp.left;
                            }
                            if (xpl != null) {
                                xpl.red = (xp == null) ? false : xp.red;
                                if ((sl = xpl.left) != null)
                                    sl.red = false;
                            }
                            if (xp != null) {
                                xp.red = false;
                                root = rotateRight(root, xp);
                            }
                            x = root;
                        }
                    }
                }
            }
        }

        /**
         * Recursive invariant check
         */
        static <K,V> boolean checkInvariants(TreeNode<K,V> t) {
            TreeNode<K,V> tp = t.parent, tl = t.left, tr = t.right,
                tb = t.prev, tn = (TreeNode<K,V>)t.next;
            if (tb != null && tb.next != t)
                return false;
            if (tn != null && tn.prev != t)
                return false;
            if (tp != null && t != tp.left && t != tp.right)
                return false;
            if (tl != null && (tl.parent != t || tl.hash > t.hash))
                return false;
            if (tr != null && (tr.parent != t || tr.hash < t.hash))
                return false;
            if (t.red && tl != null && tl.red && tr != null && tr.red)
                return false;
            if (tl != null && !checkInvariants(tl))
                return false;
            if (tr != null && !checkInvariants(tr))
                return false;
            return true;
        }
