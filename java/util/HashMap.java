    /**
     * Implements Map.remove and related methods
     *
     * @param hash hash for key
     * @param key the key
     * @param value the value to match if matchValue, else ignored
     * @param matchValue if true only remove if value is equal
     * @param movable if false do not move other nodes while removing
     * @return the node, or null if none
     */
    /*
     * 删除主要分为两部分，
     * 第一部分是要找到需要删除的key
     * 第二部分是做删除处理
     * 在查找方面，提供了 matchValue 参数用于控制是否对 value 做比较，如果为 true 则 key 和 value 都相等的情况下才可以删除
     * 而 movable 参数则是针对红黑树的情况下做的控制
     */
    final Node<K,V> removeNode(int hash, Object key, Object value,
                               boolean matchValue, boolean movable) {
        Node<K,V>[] tab; Node<K,V> p; int n, index;
        if ((tab = table) != null && (n = tab.length) > 0 &&
            (p = tab[index = (n - 1) & hash]) != null) {
            //删除操作必须建立在 hash 槽不为 null 且 能找到 hash 值形同的数据的情况下
            //否则直接返回 null 即可
            Node<K,V> node = null, e; K k; V v;
            if (p.hash == hash &&
                ((k = p.key) == key || (key != null && key.equals(k))))
                //一般都会对第一个数据做单独的比较处理
                //这里是对应的 hash槽上的第一个元素判断是否相同
                node = p;
            else if ((e = p.next) != null) {
                //第一个不匹配，继续往下找
                if (p instanceof TreeNode)
                    //红黑树查找
                    node = ((TreeNode<K,V>)p).getTreeNode(hash, key);
                else {
                    //普通链表查找
                    //这里需要注意的是，在查找到元素时 p 时其上一个元素
                    do {
                        if (e.hash == hash &&
                            ((k = e.key) == key ||
                             (key != null && key.equals(k)))) {
                            node = e;
                            break;
                        }
                        p = e;
                    } while ((e = e.next) != null);
                }
            }
            //上面做的一系列工作都是为了找到需要去删除的点。
            //如果找到了，用 node 变量存储它
            if (node != null && (!matchValue || (v = node.value) == value ||
                                 (value != null && value.equals(v)))) {
                //这里做了非空、值匹配处理
                if (node instanceof TreeNode)
                    //红黑树移出节点，注意这里的 movable 参数
                    ((TreeNode<K,V>)node).removeTreeNode(this, tab, movable);
                else if (node == p)
                    // 这种情况下 p 是 hash 槽的第一个元素，如果第一个元素就是需要删除的元素
                    // 则直接 把 hash 槽第一个存储的元素变为 p.next
                    tab[index] = node.next;
                else
                    //这种情况下 p 是 node 的前一个元素，直接讲 p.next 指向 node.next 即可
                    p.next = node.next;
                //常规的几个操作
                ++modCount;
                --size;
                afterNodeRemoval(node);
                return node;
            }
        }
        return null;
    }

    /**
     * Removes all of the mappings from this map.
     * The map will be empty after this call returns.
     */
    public void clear() {
        /*
         * clear 这里都没有缩减容量的操作
         * 只是将 size 设置为 0，同时将所有槽位存储为 Null 便于垃圾回收
         */
        Node<K,V>[] tab;
        modCount++;
        if ((tab = table) != null && size > 0) {
            size = 0;
            for (int i = 0; i < tab.length; ++i)
                tab[i] = null;
        }
    }

    /**
     * Returns <tt>true</tt> if this map maps one or more keys to the
     * specified value.
     *
     * @param value value whose presence in this map is to be tested
     * @return <tt>true</tt> if this map maps one or more keys to the
     *         specified value
     */
    public boolean containsValue(Object value) {
        Node<K,V>[] tab; V v;
        if ((tab = table) != null && size > 0) {
            for (int i = 0; i < tab.length; ++i) {
                for (Node<K,V> e = tab[i]; e != null; e = e.next) {
                    if ((v = e.value) == value ||
                        (value != null && value.equals(v)))
                        return true;
                }
            }
        }
        return false;
    }

